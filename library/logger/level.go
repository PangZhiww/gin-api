package logger

type Level int8

const (
	UnknownLevel Level = iota - 1

	DebugLevel

	InfoLevel

	WarnLevel

	ErrorLevel

	FatalLevel
)

func (l Level) String() string {
	switch l {
	case DebugLevel:
		return "debug"
	case InfoLevel:
		return "info"
	case WarnLevel:
		return "warn"
	case ErrorLevel:
		return "error"
	case FatalLevel:
		return "fatal"
	default:
		return "unknown"
	}
}

func StringToLevel(l string) Level {
	switch l {
	case "debug":
		return DebugLevel
	case "info":
		return InfoLevel
	case "warn":
		return WarnLevel
	case "error":
		return ErrorLevel
	case "fatal":
		return FatalLevel
	default:
		return UnknownLevel
	}
}
